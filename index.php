                    <li>Повышенная регулярная работа с тяжестями, неправильно распределенные нагрузки на суставы;</li>
                    <li>Нарушение обмена веществ по разным причинам. Восточная медицина называет это нарушением доши «Слизь»;</li>
                    <li>Cбои в работе печени и желчного пузыря (вост. жар или холод печени) проявляются слабостью связочного аппарата и мышц. Нарушается регенерация хрящей. Это также приводит к повышенной нагрузке на суставы;</li>
                    <li>Нарушение работы почек (вост. холод почек), что приводит к вымыванию кальция, калия, магния, склонностью к воспалительным процессам.</li>
